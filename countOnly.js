const countOnly = function(totalItems, countingItems) {
  const obj = {};
  for (const key of totalItems) {
    if (countingItems[key]) {
      if (obj[key]) {
        obj[key] += 1;
      } else {
        obj[key] = 1;
      }
    }
  }
  return obj;
};

